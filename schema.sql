        ROLL_NO = p_roll_no,
        DEPARTMENT = p_dept,
        SEMESTER = p_sem,
        EMAIL = p_email,
        PHONE_NUMBER = p_phone,
        UPDATED_AT = CURRENT_TIMESTAMP
