        p_message := 'Invalid Phone Number. It must be exactly 10 digits.';
        RETURN;
    END IF;

    INSERT INTO STUDENTS (STUDENT_NAME, ROLL_NO, DEPARTMENT, SEMESTER, EMAIL, PHONE_NUMBER)
    VALUES (p_name, p_roll_no, p_dept, p_sem, p_email, p_phone);
    
    COMMIT;
    p_status := 'SUCCESS';
    p_message := 'Student registered successfully.';
EXCEPTION
    WHEN DUP_VAL_ON_INDEX THEN
        ROLLBACK;
        p_status := 'ERROR';
        p_message := 'A student with this Roll No or Email already exists.';
    WHEN OTHERS THEN
        ROLLBACK;
        p_status := 'ERROR';
        p_message := 'Database error: ' || SQLERRM;
END;
