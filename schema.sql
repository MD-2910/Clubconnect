-- Procedure to update a student
CREATE OR REPLACE PROCEDURE proc_update_student(
    p_student_id IN NUMBER,
    p_name IN VARCHAR2,
    p_roll_no IN VARCHAR2,
    p_dept IN VARCHAR2,
    p_sem IN NUMBER,
    p_email IN VARCHAR2,
    p_phone IN VARCHAR2,
    p_status OUT VARCHAR2,
    p_message OUT VARCHAR2
) IS
BEGIN
