-- Auto-create APP_USERS record for new clubs
CREATE OR REPLACE TRIGGER trg_auto_create_club_user
AFTER INSERT ON CLUBS
FOR EACH ROW
DECLARE
    v_base_username VARCHAR2(50);
BEGIN
    v_base_username := LOWER(REGEXP_SUBSTR(:NEW.CLUB_NAME, '^[^ ]+'));
    
    INSERT INTO APP_USERS (
        USERNAME, 
        PASSWORD, 
        ROLE, 
        CLUB_ID
    ) VALUES (
        v_base_username, 
        LOWER(RAWTOHEX(STANDARD_HASH(v_base_username || 'club', 'SHA256'))), 
        'CLUB', 
        :NEW.CLUB_ID
    );
EXCEPTION
    WHEN DUP_VAL_ON_INDEX THEN
        -- If the username already exists, append the CLUB_ID to make it unique natively
        INSERT INTO APP_USERS (
            USERNAME, 
            PASSWORD, 
            ROLE, 
            CLUB_ID
        ) VALUES (
            v_base_username || :NEW.CLUB_ID, 
            LOWER(RAWTOHEX(STANDARD_HASH(v_base_username || 'club', 'SHA256'))), 
            'CLUB', 
            :NEW.CLUB_ID
        );
END;
