-- Function to check whether a student can register for an event
CREATE OR REPLACE FUNCTION check_registration_eligibility(
    p_event_id IN NUMBER,
    p_student_id IN NUMBER
) RETURN VARCHAR2 IS
    v_event_status VARCHAR2(20);
    v_reg_deadline DATE;
    v_max_participants NUMBER;
    v_current_registrations NUMBER;
    v_existing_reg_status VARCHAR2(20);
BEGIN
    -- Check if student is already registered
    BEGIN
        SELECT REGISTRATION_STATUS INTO v_existing_reg_status
        FROM REGISTRATIONS 
        WHERE EVENT_ID = p_event_id AND STUDENT_ID = p_student_id;
        
        IF v_existing_reg_status = 'REGISTERED' THEN
            RETURN 'ALREADY_REGISTERED';
        END IF;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            NULL; -- Student not registered, proceed
    END;

    -- Get event details
    BEGIN
        SELECT EVENT_STATUS, REGISTRATION_DEADLINE, MAX_PARTICIPANTS
        INTO v_event_status, v_reg_deadline, v_max_participants
        FROM EVENTS
        WHERE EVENT_ID = p_event_id;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RETURN 'EVENT_NOT_FOUND';
    END;
    
    -- Check event status
    IF v_event_status != 'UPCOMING' THEN
        RETURN 'EVENT_NOT_UPCOMING';
    END IF;

    -- Check registration deadline
    IF v_reg_deadline IS NOT NULL AND v_reg_deadline < TRUNC(SYSDATE) THEN
        RETURN 'DEADLINE_PASSED';
    END IF;

    -- Check participant limit
    IF v_max_participants IS NOT NULL THEN
        SELECT COUNT(*) INTO v_current_registrations
        FROM REGISTRATIONS
        WHERE EVENT_ID = p_event_id AND REGISTRATION_STATUS = 'REGISTERED';
        
        IF v_current_registrations >= v_max_participants THEN
            RETURN 'EVENT_FULL';
        END IF;
    END IF;

    RETURN 'ELIGIBLE';
END;
/

-- Procedure to register a student for an event
CREATE OR REPLACE PROCEDURE register_for_event(
    p_event_id IN NUMBER,
    p_student_id IN NUMBER,
    p_status OUT VARCHAR2,
    p_message OUT VARCHAR2
) IS
    v_eligibility VARCHAR2(50);
    v_existing_count NUMBER;
BEGIN
    -- Check eligibility
    v_eligibility := check_registration_eligibility(p_event_id, p_student_id);
    
    IF v_eligibility = 'ELIGIBLE' THEN
        -- Check if a cancelled registration exists
        SELECT COUNT(*) INTO v_existing_count
        FROM REGISTRATIONS
        WHERE EVENT_ID = p_event_id AND STUDENT_ID = p_student_id;
        
        IF v_existing_count > 0 THEN
            -- Update cancelled registration to registered
            UPDATE REGISTRATIONS
            SET REGISTRATION_STATUS = 'REGISTERED',
                REGISTRATION_DATE = CURRENT_TIMESTAMP
            WHERE EVENT_ID = p_event_id AND STUDENT_ID = p_student_id;
        ELSE
            -- Insert new registration
            INSERT INTO REGISTRATIONS (EVENT_ID, STUDENT_ID, REGISTRATION_STATUS)
            VALUES (p_event_id, p_student_id, 'REGISTERED');
        END IF;
        
        COMMIT;
        p_status := 'SUCCESS';
        p_message := 'Successfully registered for the event.';
    ELSE
        p_status := 'ERROR';
        IF v_eligibility = 'ALREADY_REGISTERED' THEN
            p_message := 'You are already registered for this event.';
        ELSIF v_eligibility = 'EVENT_NOT_FOUND' THEN
            p_message := 'The specified event does not exist.';
        ELSIF v_eligibility = 'EVENT_NOT_UPCOMING' THEN
            p_message := 'This event is no longer accepting registrations.';
        ELSIF v_eligibility = 'DEADLINE_PASSED' THEN
            p_message := 'The registration deadline for this event has passed.';
        ELSIF v_eligibility = 'EVENT_FULL' THEN
            p_message := 'This event has reached its maximum capacity.';
        ELSE
            p_message := 'Registration failed due to an unknown error.';
        END IF;
    END IF;
EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        p_status := 'ERROR';
        p_message := 'An unexpected error occurred: ' || SQLERRM;
END;
/

-- Function to check if a venue is available during a specific time for an event
CREATE OR REPLACE FUNCTION check_venue_availability(
    p_venue_id IN NUMBER,
    p_event_date IN DATE,
    p_start_time IN TIMESTAMP,
    p_end_time IN TIMESTAMP
) RETURN VARCHAR2 IS
    v_conflict_count NUMBER;
    v_venue_active VARCHAR2(1);
BEGIN
    -- Check if venue exists and is active
    BEGIN
        SELECT IS_ACTIVE INTO v_venue_active
        FROM VENUES
        WHERE VENUE_ID = p_venue_id;
        
        IF v_venue_active = 'N' THEN
            RETURN 'VENUE_INACTIVE';
        END IF;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RETURN 'VENUE_NOT_FOUND';
    END;

    -- Check for overlapping events at the same venue on the same date
    -- (Assuming an event is active/upcoming; cancelled ones might not conflict)
    SELECT COUNT(*) INTO v_conflict_count
    FROM EVENTS
    WHERE VENUE_ID = p_venue_id
      AND EVENT_DATE = p_event_date
      AND EVENT_STATUS != 'CANCELLED'
      AND (
          (p_start_time >= START_TIME AND p_start_time < END_TIME) OR
          (p_end_time > START_TIME AND p_end_time <= END_TIME) OR
          (p_start_time <= START_TIME AND p_end_time >= END_TIME)
      );

    IF v_conflict_count > 0 THEN
        RETURN 'VENUE_UNAVAILABLE';
    END IF;

    RETURN 'AVAILABLE';
END;
/

-- Procedure to create an event
CREATE OR REPLACE PROCEDURE create_event(
    p_club_id IN NUMBER,
    p_venue_id IN NUMBER,
    p_event_name IN VARCHAR2,
    p_event_category IN VARCHAR2,
    p_event_date IN DATE,
    p_start_time IN TIMESTAMP,
    p_end_time IN TIMESTAMP,
    p_description IN VARCHAR2,
    p_registration_deadline IN DATE,
    p_max_participants IN NUMBER,
    p_status OUT VARCHAR2,
    p_message OUT VARCHAR2,
    p_new_event_id OUT NUMBER
) IS
    v_venue_status VARCHAR2(50);
    v_club_status VARCHAR2(20);
BEGIN
    -- 1. Validate dates and times
    IF p_event_date < TRUNC(SYSDATE) THEN
        p_status := 'ERROR';
        p_message := 'Event date cannot be in the past.';
        RETURN;
    END IF;
    
    IF p_start_time >= p_end_time THEN
        p_status := 'ERROR';
        p_message := 'End time must be after start time.';
        RETURN;
    END IF;
    
    IF p_registration_deadline IS NOT NULL AND p_registration_deadline > p_event_date THEN
        p_status := 'ERROR';
        p_message := 'Registration deadline cannot be after the event date.';
        RETURN;
    END IF;

    -- 2. Validate club
    BEGIN
        SELECT STATUS INTO v_club_status
        FROM CLUBS
        WHERE CLUB_ID = p_club_id;
        
        IF v_club_status != 'Active' THEN
            p_status := 'ERROR';
            p_message := 'Only active clubs can create events.';
            RETURN;
        END IF;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            p_status := 'ERROR';
            p_message := 'Club not found.';
            RETURN;
    END;

    -- 3. Validate venue availability
    v_venue_status := check_venue_availability(p_venue_id, p_event_date, p_start_time, p_end_time);
    
    IF v_venue_status = 'VENUE_NOT_FOUND' THEN
        p_status := 'ERROR';
        p_message := 'Venue not found.';
        RETURN;
    ELSIF v_venue_status = 'VENUE_INACTIVE' THEN
        p_status := 'ERROR';
        p_message := 'The selected venue is currently inactive.';
        RETURN;
    ELSIF v_venue_status = 'VENUE_UNAVAILABLE' THEN
        p_status := 'ERROR';
        p_message := 'The venue is already booked for the selected time slot.';
        RETURN;
    END IF;

    -- 4. Create the event
    INSERT INTO EVENTS (
        CLUB_ID, VENUE_ID, EVENT_NAME, EVENT_CATEGORY, EVENT_DATE, 
        START_TIME, END_TIME, DESCRIPTION, EVENT_STATUS, 
        REGISTRATION_DEADLINE, MAX_PARTICIPANTS
    ) VALUES (
        p_club_id, p_venue_id, p_event_name, p_event_category, p_event_date,
        p_start_time, p_end_time, p_description, 'UPCOMING',
        p_registration_deadline, p_max_participants
    ) RETURNING EVENT_ID INTO p_new_event_id;

    COMMIT;
    
    p_status := 'SUCCESS';
    p_message := 'Event created successfully. Event ID: ' || p_new_event_id;

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        p_status := 'ERROR';
        p_message := 'An unexpected error occurred: ' || SQLERRM;
        p_new_event_id := NULL;
END;
/

-- Procedure to delete (cancel) an event by a club
CREATE OR REPLACE PROCEDURE delete_event_by_club(
    p_event_id IN NUMBER,
    p_club_id IN NUMBER,
    p_status OUT VARCHAR2,
    p_message OUT VARCHAR2
) IS
    v_event_club_id NUMBER;
    v_event_status VARCHAR2(20);
BEGIN
    -- 1. Check if event exists and get its status and club_id
    BEGIN
        SELECT CLUB_ID, EVENT_STATUS INTO v_event_club_id, v_event_status
        FROM EVENTS
        WHERE EVENT_ID = p_event_id;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            p_status := 'ERROR';
            p_message := 'Event not found.';
            RETURN;
    END;

    -- 2. Verify that the club owns this event
    IF v_event_club_id != p_club_id THEN
        p_status := 'ERROR';
        p_message := 'Unauthorized. Only the organizing club can delete this event.';
        RETURN;
    END IF;

    -- 3. Check event status
    IF v_event_status = 'CANCELLED' THEN
        p_status := 'ERROR';
        p_message := 'This event is already cancelled.';
        RETURN;
    ELSIF v_event_status = 'COMPLETED' THEN
        p_status := 'ERROR';
        p_message := 'Cannot delete a completed event.';
        RETURN;
    END IF;

    -- 4. Cancel the event securely
    UPDATE EVENTS
    SET EVENT_STATUS = 'CANCELLED',
        UPDATED_AT = CURRENT_TIMESTAMP
    WHERE EVENT_ID = p_event_id;

    -- 5. Cancel any active registrations for this event
    UPDATE REGISTRATIONS
    SET REGISTRATION_STATUS = 'CANCELLED'
    WHERE EVENT_ID = p_event_id;

    COMMIT;
    
    p_status := 'SUCCESS';
    p_message := 'Event successfully deleted (cancelled) and registrations updated.';

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        p_status := 'ERROR';
        p_message := 'An unexpected error occurred: ' || SQLERRM;
END;
/

