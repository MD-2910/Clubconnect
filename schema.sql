        COMMIT;
        p_status := 'SUCCESS';
        p_message := 'Student updated successfully.';
    END IF;
EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        p_status := 'ERROR';
        p_message := 'Database error: ' || SQLERRM;
END;
