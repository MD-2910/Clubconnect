CREATE TABLE STUDENTS (
    STUDENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    STUDENT_NAME VARCHAR2(100) NOT NULL,
    ROLL_NO VARCHAR2(20) UNIQUE NOT NULL CHECK (REGEXP_LIKE(ROLL_NO, '^AU[0-9]{7}$')),
    DEPARTMENT VARCHAR2(100),
    SEMESTER NUMBER CHECK (SEMESTER BETWEEN 1 AND 12),
    EMAIL VARCHAR2(100) UNIQUE NOT NULL CHECK (EMAIL LIKE '%@ahduni.edu.in'),
    PHONE_NUMBER VARCHAR2(20) NOT NULL CHECK (REGEXP_LIKE(PHONE_NUMBER, '^[0-9]{10}$')),
    STATUS VARCHAR2(20) DEFAULT 'Active' CHECK (STATUS IN ('Active', 'Inactive', 'Alumni')),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE CLUBS (
    CLUB_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CLUB_NAME VARCHAR2(100) NOT NULL,
    CLUB_TYPE VARCHAR2(50),
    DESCRIPTION VARCHAR2(1000),
    EMAIL VARCHAR2(100) UNIQUE NOT NULL CHECK (EMAIL LIKE '%@ahduni.edu.in'),
    CONTACT_NUMBER VARCHAR2(20) NOT NULL CHECK (REGEXP_LIKE(CONTACT_NUMBER, '^[0-9]{10}$')),
    FOUNDED_YEAR NUMBER,
    STATUS VARCHAR2(20) DEFAULT 'Active' CHECK (STATUS IN ('Active', 'Inactive')),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE APP_USERS (
    USER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME VARCHAR2(50) UNIQUE NOT NULL,
    PASSWORD VARCHAR2(255) NOT NULL,
    ROLE VARCHAR2(20) CHECK (ROLE IN ('ADMIN', 'CLUB', 'STUDENT')) NOT NULL,
    CLUB_ID NUMBER REFERENCES CLUBS(CLUB_ID) ON DELETE CASCADE,
    STUDENT_ID NUMBER REFERENCES STUDENTS(STUDENT_ID) ON DELETE CASCADE,
    IS_ACTIVE VARCHAR2(1) DEFAULT 'Y' CHECK (IS_ACTIVE IN ('Y', 'N')),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_app_users_role CHECK (
        (ROLE = 'ADMIN' AND CLUB_ID IS NULL AND STUDENT_ID IS NULL) OR
        (ROLE = 'CLUB' AND CLUB_ID IS NOT NULL AND STUDENT_ID IS NULL) OR
        (ROLE = 'STUDENT' AND STUDENT_ID IS NOT NULL AND CLUB_ID IS NULL)
    )
);

CREATE TABLE VENUES (
    VENUE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VENUE_NAME VARCHAR2(100) NOT NULL,
    LOCATION VARCHAR2(200),
    CAPACITY NUMBER CHECK (CAPACITY > 0),
    VENUE_TYPE VARCHAR2(50),
    IS_ACTIVE VARCHAR2(1) DEFAULT 'Y' CHECK (IS_ACTIVE IN ('Y', 'N'))
);

CREATE TABLE EVENTS (
    EVENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CLUB_ID NUMBER REFERENCES CLUBS(CLUB_ID) ON DELETE CASCADE,
    VENUE_ID NUMBER REFERENCES VENUES(VENUE_ID),
    EVENT_NAME VARCHAR2(200) NOT NULL,
    EVENT_CATEGORY VARCHAR2(50) CHECK (EVENT_CATEGORY IN ('TECHNICAL', 'CULTURAL', 'SPORTS', 'LITERARY', 'WORKSHOP', 'SEMINAR', 'COMPETITION')),
    EVENT_DATE DATE NOT NULL,
    START_TIME TIMESTAMP NOT NULL,
    END_TIME TIMESTAMP NOT NULL,
    DESCRIPTION VARCHAR2(2000),
    EVENT_STATUS VARCHAR2(20) DEFAULT 'UPCOMING' CHECK (EVENT_STATUS IN ('UPCOMING', 'COMPLETED', 'CANCELLED')),
    REGISTRATION_DEADLINE DATE,
    MAX_PARTICIPANTS NUMBER CHECK (MAX_PARTICIPANTS > 0),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_event_time CHECK (END_TIME > START_TIME),
    CONSTRAINT chk_registration_deadline CHECK (REGISTRATION_DEADLINE IS NULL OR REGISTRATION_DEADLINE <= EVENT_DATE)
);

CREATE TABLE REGISTRATIONS (
