    IF p_roll_no IS NULL OR NOT REGEXP_LIKE(p_roll_no, '^AU[0-9]{7}$') THEN
        p_status := 'ERROR';
        p_message := 'Invalid Roll No format. It should be AU followed by 7 digits.';
