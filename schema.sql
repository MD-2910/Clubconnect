    IF p_phone IS NULL OR NOT REGEXP_LIKE(p_phone, '^[0-9]{10}$') THEN
        p_status := 'ERROR';
        p_message := 'Invalid Phone Number. It must be exactly 10 digits.';
        RETURN;
    END IF;

    UPDATE STUDENTS 
