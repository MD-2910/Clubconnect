    BEGIN
        SELECT STUDENT_ID INTO v_student_id 
        FROM STUDENTS 
        WHERE ROLL_NO = p_roll_no;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            p_status := 'ERROR';
