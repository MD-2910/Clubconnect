        VALUES (p_event_id, v_student_id, 'PRESENT');
        COMMIT;
        p_status := 'SUCCESS';
        p_message := 'Attendance marked successfully for ' || p_roll_no;
    EXCEPTION
        WHEN DUP_VAL_ON_INDEX THEN
            p_status := 'SUCCESS';
            p_message := 'Attendance already marked for ' || p_roll_no;
    END;
EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        p_status := 'ERROR';
        p_message := 'Database error: ' || SQLERRM;
END;
